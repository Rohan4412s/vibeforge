def print_file_tree(files: list[ParsedFile], project_name: str):
    """Print a beautiful file tree using Rich."""
    tree = Tree(f"📁 [bold cyan]{project_name}[/bold cyan]")
    
    # Build directory structure
    dirs = {}
    for f in sorted(files, key=lambda x: x.path):
        parts = f.path.split("/")
        current = tree
        for i, part in enumerate(parts):
            if i < len(parts) - 1:
                # Directory
                key = "/".join(parts[: i + 1])
                if key not in dirs:
                    dirs[key] = current.add(f"📂 [blue]{part}[/blue]")
                current = dirs[key]
            else:
                # File
                icon = _get_file_icon(part)
                current.add(f"{icon} [green]{part}[/green]")

    console.print(tree)


